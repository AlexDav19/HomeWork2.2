public class PrintStudent {
    public void printStudent(Gryffindor[] gryffindors) {
        for (Gryffindor gryffindor : gryffindors) {
            System.out.println(gryffindor);
        }
    }

    public void printStudent(Hufflepuff[] hufflepuffs) {
        for (Hufflepuff hufflepuff : hufflepuffs) {
            System.out.println(hufflepuff);
        }
    }

    public void printStudent(Ravenclaw[] ravenclaws) {
        for (Ravenclaw ravenclaw : ravenclaws) {
            System.out.println(ravenclaw);
        }
    }

    public void printStudent(Slytherin[] slytherins) {
        for (Slytherin slytherin : slytherins) {
            System.out.println(slytherin);
        }
    }
}
